#include <iostream>

using namespace std;

int main() {
    cout << "This is a message using escape sequences:\n";
    cout << "This message contains:\n";
    cout << "\t- New line: Hello\n\t  World!\n";
    cout << "\t- Tab: This is\t a tabbed text.\n";
    cout << "\t- Backslash: This is a backslash: \\ \n";
    cout << "\t- Double quotes: This is in \"double quotes\".\n";
    return 0;
}
